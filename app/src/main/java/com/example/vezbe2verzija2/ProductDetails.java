package com.example.vezbe2verzija2;

import android.os.Bundle;
import android.widget.TextView;

import androidx.activity.EdgeToEdge;
import androidx.appcompat.app.AppCompatActivity;
import androidx.core.graphics.Insets;
import androidx.core.view.ViewCompat;
import androidx.core.view.WindowInsetsCompat;

public class ProductDetails extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_product_details);

        TextView nametext = findViewById(R.id.nazivtext);
        TextView desctext = findViewById(R.id.detaljitext);
        TextView cenatext = findViewById(R.id.cenatext);

        nametext.setText(getIntent().getStringExtra("name"));
        desctext.setText(getIntent().getStringExtra("desc"));
        cenatext.setText(getIntent().getStringExtra("price"));

        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main), (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });
    }
}